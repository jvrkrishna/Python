# Polymorphism
'''
The word polymorphism comes from the Greek words "poly", meaning "many", and "morph", meaning "form". Therefore, polymorphism means "many forms."

In object-oriented programming (OOP), polymorphism refers to the ability of a single object, method, or interface to take on different forms or perform different behaviors depending on the context.

Example: A person can perform different roles in different situations. The same person may act as a teacher at school, a father at home, and an employee at the workplace. Although the person is the same, they have different roles or forms. This is a simple real-life example of polymorphism.'''