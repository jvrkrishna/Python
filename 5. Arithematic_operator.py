a=10
b=20
# Addition(+)
add=a+b
print(add)

# Subtraction (-)
sub=b-a
print(sub)

# Multiplication (*)
mul=a*b
print(mul)

# Division (/)
div=b/a
print(div)

# Floor Division (//) It does not give any decimal value.
fdiv=b//a
print(fdiv)

# modulas (%)
mod=b%a
print(mod)

# power/square (**)
pow=a**b
print(pow)